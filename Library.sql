CREATE DATABASE LIBRARY;
USE LIBRARY;

CREATE TABLE BOOKPUBLISH (
    BookID INT,
    Title VARCHAR(100),
    AuthorID INT,
    AuthorName VARCHAR(50),
    ISBN VARCHAR(20),
    Publisher VARCHAR(100),
    PublicationYear INT,
    Pages INT,
    Language VARCHAR(50),
    CopiesAvailable INT
);

